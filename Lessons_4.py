# 1) У вас есть переменная value, тип - int. Написать тернарный оператор для
# переменной new_value по такому правилу: если value меньше 100, то new_value равно половине
# значения value, в противном случае - противоположне value число

value = int(input("Уведите целое число"))
if value < 100:
    new_value = value / 2
else:
    new_value = value * -1
print(new_value)

