# 5) У вас есть переменная my_str, тип - str. Если ее длинна меньше 5, то допишите в конец строки
# перевернутую себя же. Пример: было - "qwer", стало - "qwerrewq". Если длинна не меньше 5, то оставить
# строку как есть.

my_str = str('qwer')
if len(my_str) < 5:
    new_str = my_str + my_str[::-1]
    print(new_str)
else:
    my_str  = my_str
    print(my_str)