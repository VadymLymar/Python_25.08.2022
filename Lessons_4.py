# 4) У вас есть переменная my_str, тип - str. Если ее длинна меньше 5, то допишите в конец строки себя же.
# Пример: было - "qwer", стало - "qwerqwer". Если длинна не меньше 5, то оставить строку как есть.

my_str = str('qwer')
if len(my_str) < 5:
    symbol = my_str * 2
else:
    symbol = my_str
print(symbol)

